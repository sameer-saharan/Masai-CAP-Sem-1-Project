import turtle
 
t = turtle.Turtle()
t.speed(6)
t.pensize(4)

#First Colour
t.penup()
t.goto(-120,80)
t.color("orange")
t.begin_fill()
t.pendown()
t.left(90)
t.forward(65)
t.right(90)
t.forward(340)
t.left(90)
t.backward(65)
t.left(90)
t.forward(340)
t.end_fill()

#Second Colour
t.penup()
